function openModal(){
    let layer = document.getElementById("layer");
    let modal = document.getElementById("modal");
    layer.style.display = "block"
    modal.style.display = "block"
}
function closeModal(){
    let layer = document.getElementById("layer");
    let modal = document.getElementById("modal");
    layer.style.display = "none"
    modal.style.display = "none"
}

